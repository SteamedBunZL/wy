package com.whoyao.common;

import android.graphics.drawable.AnimationDrawable;

/**
 * @author hyh 
 * creat_at：2013-8-27-下午4:19:00
 */
public class AnimPost implements Runnable {
	
	private AnimationDrawable mAnim;

	public AnimPost(AnimationDrawable anim) {
		mAnim = anim;
	}

	@Override
	public void run() {
		mAnim.start();
	}
}