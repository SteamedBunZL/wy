package com.whoyao.model;


public class PrivateListItem {
	private int SenderId;
	private int ReceiverId;
	private String UserName;
	private String HeadPic;
	private int UnReadCount;
	private String SendTime;
	private String Content;
	private int MessId;
	private int TotalCount;
	private String HeadPic_bySelf;
	public int getSenderId() {
		return SenderId;
	}
	public void setSenderId(int senderId) {
		SenderId = senderId;
	}
	public int getReceiverId() {
		return ReceiverId;
	}
	public void setReceiverId(int receiverId) {
		ReceiverId = receiverId;
	}
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public String getHeadPic() {
		return HeadPic;
	}
	public void setHeadPic(String headPic) {
		HeadPic = headPic;
	}
	public int getUnReadCount() {
		return UnReadCount;
	}
	public void setUnReadCount(int unReadCount) {
		UnReadCount = unReadCount;
	}
	public String getSendTime() {
		return SendTime;
	}
	public void setSendTime(String sendTime) {
		SendTime = sendTime;
	}
	public String getContent() {
		return Content;
	}
	public void setContent(String content) {
		Content = content;
	}
	public int getMessId() {
		return MessId;
	}
	public void setMessId(int messId) {
		MessId = messId;
	}
	@Override
	public String toString() {
		return "PrivateListItem [SenderId=" + SenderId + ", ReceiverId="
				+ ReceiverId + ", UserName=" + UserName + ", HeadPic="
				+ HeadPic + ", UnReadCount=" + UnReadCount + ", SendTime="
				+ SendTime + ", Content=" + Content + ", MessId=" + MessId
				+ "]";
	}
	public String getHeadPic_bySelf() {
		return HeadPic_bySelf;
	}
	public void setHeadPic_bySelf(String headPic_bySelf) {
		HeadPic_bySelf = headPic_bySelf;
	}
	public int getTotalCount() {
		return TotalCount;
	}
	public void setTotalCount(int totalCount) {
		TotalCount = totalCount;
	}
	

}
