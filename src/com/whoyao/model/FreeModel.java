public class FreeModel {
	private int FreeId;
	private String FreeTimeStart;
	private String FreeTimeEnd;
	private String FreeTimeContent;
	
	public int getFreeId() {
		return FreeId;
	}
	public void setFreeId(int freeId) {
		FreeId = freeId;
	}
	public String getFreeTimeStart() {
		return FreeTimeStart;
	}
	public void setFreeTimeStart(String freeTimeStart) {
		FreeTimeStart = freeTimeStart;
	}
	public String getFreeTimeEnd() {
		return FreeTimeEnd;
	}
	public void setFreeTimeEnd(String freeTimeEnd) {
		FreeTimeEnd = freeTimeEnd;
	}
	public String getFreeTimeContent() {
		return FreeTimeContent;
	}
	public void setFreeTimeContent(String freeTimeContent) {
		FreeTimeContent = freeTimeContent;
	}
		
}
