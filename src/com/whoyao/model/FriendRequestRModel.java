package com.whoyao.model;

/**
 * 对好友请求的操作
 * @author hyh 
 * creat_at：2013-11-11-下午4:37:31
 */
public class FriendRequestRModel {
	private int frienduserid;
	private int isagree;//1：同意；0：忽略
	private int messcontentid;

	public FriendRequestRModel(int frienduserid, int isagree, int messcontentid) {
		super();
		this.frienduserid = frienduserid;
		this.isagree = isagree;
		this.messcontentid = messcontentid;
	}
	public void setFrienduserid(int frienduserid) {
		this.frienduserid = frienduserid;
	}
	public void setMesscontentid(int messcontentid) {
		this.messcontentid = messcontentid;
	}
	public void setIsagree(int isagree) {
		this.isagree = isagree;
	}
}
