package com.whoyao.model;
/**
 * @author hyh 
 * creat_at：2013-7-15-下午2:50:29 <p>
 * 检查账号是否已被注册
 */
public class CheckAccountResponseModel {
	public int userPhone;
	public int userEmail;
	public int userNickName;
	
	public boolean isPhoneRegistered(){
		return 0!=userPhone;
	}
	public boolean isEmailRegistered(){
		return 0!=userEmail;
	}
	public boolean isNickRegistered(){
		return 0!=userNickName;
	}
}
