public class CartAddItemTModel {
	private int venueid;
	private int placetype;
	private int placetypedetail;
	private String placedate;
	private float price;
	private int placetime;
	private int count;
	private int placestatisticid;
	
	public CartAddItemTModel(int id,PlaceStatisticModel data) {
		venueid = id;
		placetype = data.getPlacetype();
		placetypedetail = data.getPlacetypedetail();
		placedate = data.getPlacedate();
		price = data.getPrice();
		placetime = data.getPlacetime();
		placestatisticid = data.getPlacestatisticid();
	}
	public int getVenueid() {
		return venueid;
	}
	public void setVenueid(int venueid) {
		this.venueid = venueid;
	}
	public int getPlacetype() {
		return placetype;
	}
	public void setPlacetype(int placetype) {
		this.placetype = placetype;
	}
	public int getPlacetypedetail() {
		return placetypedetail;
	}
	public void setPlacetypedetail(int placetypedetail) {
		this.placetypedetail = placetypedetail;
	}
	public String getPlacedate() {
		return placedate;
	}
	public void setPlacedate(String placedate) {
		this.placedate = placedate;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public int getPlacetime() {
		return placetime;
	}
	public void setPlacetime(int placetime) {
		this.placetime = placetime;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getPlacestatisticid() {
		return placestatisticid;
	}
	public void setPlacestatisticid(int placestatisticid) {
		this.placestatisticid = placestatisticid;
	}
	@Override
	public String toString() {
		return "CartAddItemTModel [venueid=" + venueid + ", placetype="
				+ placetype + ", placetypedetail=" + placetypedetail
				+ ", placedate=" + placedate + ", price=" + price
				+ ", placetime=" + placetime + ", count=" + count
				+ ", placestatisticid=" + placestatisticid + "]";
	}
	
}
