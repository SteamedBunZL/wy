package com.whoyao.venue.model;

import java.io.Serializable;
import java.util.ArrayList;

public class SiteVenueItem implements Serializable{
	private int venueid;
	private float placeprice;
	private String fullname;
	private String address;
	private float distance;
	private float latitude;
	private float longitude;
	private String logourl;
	ArrayList<PlaceList> placelist;
	ArrayList<ServiceTypeList> servicetype;

	public int getVenueid() {
		return venueid;
	}

	public void setVenueid(int venueid) {
		this.venueid = venueid;
	}

	public float getPlaceprice() {
		return placeprice;
	}

	public void setPlaceprice(float placeprice) {
		this.placeprice = placeprice;
	}

	public String getFullname() {
		return fullname;
	}

	public void setFullname(String fullname) {
		this.fullname = fullname;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public float getDistance() {
		return distance;
	}

	public void setDistance(float distance) {
		this.distance = distance;
	}

	public float getLatitude() {
		return latitude;
	}

	public void setLatitude(float latitude) {
		this.latitude = latitude;
	}

	public float getLongitude() {
		return longitude;
	}

	public void setLongitude(float longitude) {
		this.longitude = longitude;
	}

	public ArrayList<PlaceList> getPlacelist() {
		return placelist;
	}

	public void setPlacelist(ArrayList<PlaceList> placelist) {
		this.placelist = placelist;
	}

	public ArrayList<ServiceTypeList> getServicetype() {
		return servicetype;
	}

	public void setServicetype(ArrayList<ServiceTypeList> servicetype) {
		this.servicetype = servicetype;
	}

	public String getLogourl() {
		return logourl;
	}

	public void setLogourl(String logourl) {
		this.logourl = logourl;
	}

}
