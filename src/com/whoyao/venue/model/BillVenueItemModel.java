package com.whoyao.venue.model;

public class BillVenueItemModel {
	private long venuenumber;
	private int time;
	private int money;
	private int remainder;


	public long getVenuenumber() {
		return venuenumber;
	}

	public void setVenuenumber(long venuenumber) {
		this.venuenumber = venuenumber;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

	public int getRemainder() {
		return remainder;
	}

	public void setRemainder(int remainder) {
		this.remainder = remainder;
	}

	
}
